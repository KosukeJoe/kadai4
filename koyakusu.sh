#!/bin/bash

# 関数: 最大公約数を求める（ユークリッドの互除法）
gcd() {
  local a=$1
  local b=$2

  while [ $b -ne 0 ]; do
    local temp=$b
    b=$((a % b))
    a=$temp
  done

  echo "$a"
}

# 入力チェック
if [ $# -ne 2 ]; then
  echo "エラー: 2つの自然数を引数として指定してください。" >&2
  exit 1
fi

# 引数が自然数かどうかをチェック（0より大きい整数）
if ! [[ "$1" =~ ^[1-9][0-9]*$ && "$2" =~ ^[1-9][0-9]*$ ]]; then
  echo "エラー: 引数は1以上の自然数である必要があります。" >&2
  exit 1
fi

# 最大公約数の計算と出力
gcd "$1" "$2"


